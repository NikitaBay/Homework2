public class PinocchioApples {
    public static void main(String[] args) {
        final int pinocchioApples = 33;
        final int karabasBarabasApples = 32;
        int leftoverApplesPinocchio = pinocchioApples - karabasBarabasApples;

        System.out.println("Яблок у Буратино: " + pinocchioApples);
        System.out.println("Яблок забрал Карабаc Барабас: " + karabasBarabasApples);
        System.out.println("Яблок осталось у Буратино: " + leftoverApplesPinocchio);
    }
}
